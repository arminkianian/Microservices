
using ApplicationService.People.CommandHandlers;
using Domain.People.Repositories;
using Infrastructure;
using Infrastructure.People;
using Microsoft.EntityFrameworkCore;

namespace WebApi
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            builder.Services.AddScoped<CreateNewPersonCommandHandler>();
            builder.Services.AddScoped<IPersonCommandRepository, PersonCommandRepository>();
            builder.Services.AddDbContext<AppDbContext>(c=> c.UseSqlServer(builder.Configuration.GetConnectionString("TestingDb")));


            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
